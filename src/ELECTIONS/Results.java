    private void GetWinner()
   {
       try{
          myCon = DriverManager.getConnection("jdbc:mysql://localhost:3306/electiondb", "root", "nush2830"); 
          St = myCon.createStatement();
          String Query = "select CandidateId, Count(CandidateId) from votestb where ElectId="+Key+" GROUP BY CandidateId ORDER BY CandidateId DESC LIMIT 1 ";
          Rs = St.executeQuery(Query);
          while (Rs.next()){
              //JOptionPane.showMessageDialog(this,""+Rs.getInt(1));
            WinnerId = Rs.getInt(1);
          }
       }catch(Exception e)
       {
         JOptionPane.showMessageDialog(this, e);  
       }
   }
    private ImageIcon ResizePhoto(String ImagePath, byte[] pic) {
          ImageIcon MyImage = null;
          if(ImagePath != null)
        {
          MyImage = new ImageIcon(ImagePath);
        } else{
             MyImage = new ImageIcon(pic);
              }
        Image img =MyImage.getImage();
        Image newImg = img.getScaledInstance(CandidatePictureLbl.getWidth(), CandidatePictureLbl.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
      }
