    private void GetVotes()
    {
        
       try{
          myCon = DriverManager.getConnection("jdbc:mysql://localhost:3306/electiondb", "root", "nush2830"); 
          St = myCon.createStatement();
          String Query = "select  Count(CandidateId) from votestb where CandidateId="+WinnerId ;
          Rs = St.executeQuery(Query);
          while (Rs.next()){
              //JOptionPane.showMessageDialog(this,""+Rs.getInt(1));
            Votes = Rs.getInt(1);
            //JOptionPane.showMessageDialog(this,""+Votes);
            votesLbl.setText(Votes+"Votes");
          }
       }catch(Exception e)
       {
         JOptionPane.showMessageDialog(this, e);  
       }
      
    }
    int TotalVotes;
    double WinPercentage;
 private void GetPercentage()
    {
        
       try{
          myCon = DriverManager.getConnection("jdbc:mysql://localhost:3306/electiondb", "root", "nush2830"); 
          St = myCon.createStatement();
          String Query = "select  Count(*) from votestb where ElectId="+Key;
          Rs = St.executeQuery(Query);
          while (Rs.next()){
              
            TotalVotes = Rs.getInt(1);
           JOptionPane.showMessageDialog(this, TotalVotes);
           WinPercentage = (Votes*100)/TotalVotes;
           percentageLbl.setText(new DecimalFormat("##.##").format(WinPercentage)+"%");
          }
       }catch(Exception e)
       {
         JOptionPane.showMessageDialog(this, e);  
       }
      
    }
